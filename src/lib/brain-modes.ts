/**
 * Режим «свободы» сборки мозга. Отдельный модуль, потому что константы нужны и
 * на клиенте (селектор в панели), а `brain.ts` тянет Supabase и серверные ключи
 * — импортировать его в компонент нельзя.
 *
 * Одна ручка управляет тремя вещами, которые всегда двигаются вместе: сколько
 * узлов брать, насколько терпеть шум и как густо связывать.
 *
 * strict   — только то, без чего картина рассыпается. Мало узлов, каждый весомый.
 * balanced — компромисс между полнотой и читаемостью (поведение по умолчанию).
 * free     — максимально полная картина: тянет всё, включая мелочи и разовое.
 */

export const BRAIN_MODES = ["strict", "balanced", "free"] as const;
export type BrainMode = (typeof BRAIN_MODES)[number];

export const BRAIN_MODE_LABEL: Record<BrainMode, string> = {
  strict: "Строгий",
  balanced: "Средний",
  free: "Свободный",
};

export const BRAIN_MODE_HINT: Record<BrainMode, string> = {
  strict: "Мало узлов, только по-настоящему важное",
  balanced: "Баланс полноты и читаемости",
  free: "Максимально полная картина по всем данным",
};

export interface ModeSpec {
  /** Диапазон узлов для полной сборки. */
  full: string;
  /** Диапазон новых узлов для дельты. */
  delta: string;
  /** Диапазон узлов-деталей для детализации категории. */
  expand: string;
  /** Что делать с пограничными сущностями. */
  rule: string;
  /** Плотность рёбер. */
  edges: string;
  /** Температура: строгому нужна предсказуемость, свободному — фантазия. */
  temperature: number;
  /** Свободному режиму нужен запас на длинный JSON. */
  maxTokens: number;
  /** Сколько файлов Drive класть в контекст и по сколько символов из каждого. */
  driveFiles: number;
  driveChars: number;
  /** Потолок всего контекста в символах (~4 симв. на токен). */
  contextChars: number;
}

